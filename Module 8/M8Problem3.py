def check_five(list):
    # takes a list as input and checks if the value 5 is in the list.

    if 5 in list:
        # if the value 5 is in the list, print "5 is in the list".
        print("5 is in the list")
    else:
        # if the value 5 is not in the list, print "5 is not in the list".
        print("5 is not in the list")

# Create a list of numbers
list = [1, 2, 3, 4, 6]

# check if 5 is in the list
check_five(list)